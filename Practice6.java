/* 
Access  Specifiers(Modifier): accessibility or scope of a field.
Java has total 11 modifers in which 4 are access modifiers and 7 are non-access moodifiers.

- Public: within a class, outside the class as well as package(sub class and non sub class).
- Protected: within a class, outside the class as well as package(only sub class(inheritence i.e child and parent class)).
- Default: within a class, outside the class but in the same package.
- Private: only within a class.

Non-access modifiers: static, abstract, synchronized, final, native, volatile, transient, strictfp.
*/